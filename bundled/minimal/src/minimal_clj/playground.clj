(ns minimal-clj.playground)

; This project is configured to autostart the repl and connect it to the editor (Jack-in)
; See .vscode/settings.json for the configuration
; See also https://calva.io/connect-sequences/
;
; Further, the project is configured to use the Output “terminal” for evaluation results,
; and side effect output. Evaluation results are also shown inline in th editor (the
; first line of the result) as well as sent to the Calva inspector.

; If you are new to Calva, you may want to use the command:
; Calva: Create a “Getting Started” REPL project
; which creates a project with a an interactive Calva (and Clojure) guide.